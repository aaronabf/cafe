$(window).on('load', function() {
  $('body').removeClass('preload');
});

$('#photo-use-header').on('click', function() {
  $('#photo-use-header').hide();
  $('#photo-use-body').show();
});

$(window).on('click', function(e) {
  if ($(e.target).is('#photo-use-close')) {
    $('#photo-use-header').show();
    $('#photo-use-body').hide();
  }
});
